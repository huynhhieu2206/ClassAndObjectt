package advance.dev;

public class Student {

	String name;
	int age;
	String DC;
	String phone;
	double DTB;

	// Constructor
	public Student(String name, int age, String DC, String phone, double DTB) {
		this.name = name;
		this.age = age;
		this.DC = DC;
		this.phone = phone;
		this.DTB = DTB;
	}
}
